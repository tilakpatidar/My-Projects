function pushbutton1_Callback(hObject, eventdata, handles)
answer=get(handles.edit12,'String');
sqlpass=num2str(handles.initial_parameter);
        set(handles.uipanel3,'Visible','off');
        set(handles.uipanel2,'Visible','on');
    set(handles.uipanel2,'Visible','on');
    javaaddpath('\myJavaClasses\mysql-connector-java-5.1.26-bin.jar')
%# connection parameteres
host = 'localhost';
user = 'root';
password =sqlpass;
dbName = 'library'; 
%# JDBC parameters
jdbcString = sprintf('jdbc:mysql://%s/%s', host, dbName);
jdbcDriver = 'com.mysql.jdbc.Driver';

%# Create the database connection object
conn = database(dbName, user , password, jdbcDriver, jdbcString);

   
        
            if isconnection(conn) % check to make sure that we successfully connected
                          
            
    qry = sprintf('SELECT * FROM MEMBER WHERE id=''%s'';',answer);
   
    rs=fetch(exec(conn, qry));
    rsdata = get(rs, 'Data');
    id=rsdata(1,1);
    name=rsdata(1,2);
    dob=rsdata(1,3);
    sex=rsdata(1,4);
 
    age=rsdata(1,5);
    address=rsdata(1,6);
    contact_no=rsdata(1,7);
    image=rsdata(1,8);
    book1=rsdata(1,9);
    
    set(handles.edit5,'String',name);
    set(handles.edit6,'String',dob);
    set(handles.edit10,'String',age);
    set(handles.edit11,'String',sex);
    set(handles.edit7,'String',address);
    set(handles.edit8,'String',contact_no);
    set(handles.edit9,'String',id);
    set(handles.edit16,'String',book1);
   
    imshow(cell2mat(image),handles.axes2);
            end

